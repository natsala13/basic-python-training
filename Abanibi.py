message = input('please enter your sentence\n')
out = str()
for i in range(0, len(message)):
    if message[i] == 'a':
        out += message[i] + "b" + message[i]
    elif message[i] == 'e':
        out += message[i] + "b" + message[i]
    elif message[i] == 'u':
        out += message[i] + "b" + message[i]
    elif message[i] == 'o':
        out += message[i] + "b" + message[i]
    elif message[i] == 'i':
        out += message[i] + "b" + message[i]
    else:
        out += message[i]
print(out)
